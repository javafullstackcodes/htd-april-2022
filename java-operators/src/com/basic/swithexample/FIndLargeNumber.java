package com.basic.swithexample;

public class FIndLargeNumber {

	public static void main(String[] args) {
		int a=10,b=7,c=6;
		if(a>b && a>c) {
			System.out.println(a+ " is max value");
		}
		if(b>a && b>c) {
			System.out.println(b+ " is max value");
		}
		
		if(c>a && c>b) {
			System.out.println(c+ " is max value");
		}
		
	}

}
