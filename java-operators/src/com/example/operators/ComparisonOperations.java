package com.example.operators;

public class ComparisonOperations {

	public static void main(String[] args) {
		int x=10;
		int y=20;
		// = assignment
		// == comparison
		System.out.println(x==y);
		System.out.println(x>y);
		System.out.println(x<y);
		System.out.println(x>=y);
		System.out.println(x<=y);
		System.out.println(x!=y);
		
	}

}
